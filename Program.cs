using System;
using Microsoft.AspNetCore.Hosting;
using System.IO;
using aspnetcoreapp;

namespace portfolio_backend_dotnet
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var host = new WebHostBuilder()
              .UseKestrel()
              .UseContentRoot(Directory.GetCurrentDirectory())
              .UseIISIntegration()
              .UseStartup<Startup>()
              .Build();

            host.Run();
        }
    }
}
